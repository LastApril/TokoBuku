    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // TODO add your handling code here:
        String id_buku = jComboBox1.getSelectedItem().toString();
        try{
            st = conn.createStatement();
            String sql = "select * from buku where id_buku = '"+id_buku+"'";
            rs = st.executeQuery(sql);
            if(rs.next()) {
                jTextField2.setText(rs.getString(2));
                jTextField3.setText(rs.getString(7));
                jTextField4.setText(rs.getString(9));
                jTextField6.setText(rs.getString(10));
                jTextField7.setText(rs.getString(11));
            }
        }catch(SQLException e) {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

