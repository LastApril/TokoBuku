    private void jTextField5KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField5KeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode() == KeyEvent.VK_ENTER) {
            int harga = Integer.parseInt(jTextField4.getText());
            int ppn = Integer.parseInt(jTextField6.getText());
            int diskon = Integer.parseInt(jTextField7.getText());
            int jumlah = Integer.parseInt(jTextField5.getText());
            int hasildiskon = harga * diskon / 100;
            int hasilppn = harga * ppn / 100;
            int hasiljml = jumlah * harga - diskon + ppn;
            jTextField8.setText(String.valueOf(hasiljml));
            jLabel14.setText("Rp."+String.valueOf(hasiljml));
        }
    }//GEN-LAST:event_jTextField5KeyPressed

